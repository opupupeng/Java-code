package com.it.demo;

public class ForDemo02 {
    //让输出1-5之间的数字和5-1之间的数字
    public static void main(String[] args) {
        for (int i=1;i<=5;i++){
            System.out.println(i+" ");
        }
        System.out.println("------------");
        for (int i=5;i>0;i--){
            System.out.println(i);
        }
    }
}
